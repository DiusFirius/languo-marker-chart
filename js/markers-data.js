/**
 * Validates the marker data array
 * @returns {Object} Validation result with success status and any errors found
 */
function validateMarkerData() {
    const errors = [];
    const warnings = [];

    // Check array length
    if (markers.length !== 240) {
        errors.push(`Expected exactly 240 markers, but found ${markers.length}`);
    }

    // Check for duplicate IDs
    const seenIds = new Set();
    const duplicateIds = new Set();

    markers.forEach((marker, index) => {
        // Validate marker object structure
        if (!marker.id || !marker.color) {
            errors.push(`Position ${index}: Missing required properties (id or color)`);
        }

        // Check for duplicate IDs
        if (seenIds.has(marker.id)) {
            duplicateIds.add(marker.id);
        } else {
            seenIds.add(marker.id);
        }

        // Validate hex color format
        const hexColorRegex = /^#[0-9A-F]{6}$/i;
        if (marker.color && !hexColorRegex.test(marker.color)) {
            errors.push(`Position ${index} (${marker.id}): Invalid hex color format "${marker.color}" (expected #RRGGBB)`);
        }
    });

    // Report duplicate IDs
    if (duplicateIds.size > 0) {
        errors.push(`Duplicate marker IDs found: ${Array.from(duplicateIds).join(', ')}`);
    }

    // Return validation result
    const isValid = errors.length === 0;

    return {
        success: isValid,
        errors: errors,
        warnings: warnings,
        markerCount: markers.length,
        uniqueIds: seenIds.size
    };
}

